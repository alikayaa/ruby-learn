Ruby is a powerful, flexible programming language you can use in 
Web development as part of the popular Ruby on Rails web framework. 

Ruby is:
High-level, meaning reading and writing Ruby is really easy—it looks a lot like regular English!
Interpreted, meaning you don’t need a compiler to write and run Ruby. Like Python.
Object-oriented, meaning it allows users to manipulate data structures called objects in order to build and 
execute programs. Everything is objects in Ruby!

Data Types in Ruby:
Ruby has 3 data types, Numbers, Strings, Booleans. Examples are below;

number = 25
is_valid = true
name = "Ramiz Karaeski"